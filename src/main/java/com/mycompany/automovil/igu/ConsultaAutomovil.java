    private void cargarTabla() {
        //Hacemos que la tabla no sea editable
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
            
        };
        
        //Ponemos titulos a las columnas
        String titulos[] ={"Id","Modelo","Marca","Motor","Color","Placas","Puertas"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //Traer los registros de la base de datos 
        List<Automovil> listaAutomoviles = control.traerAutos();
        
        //Setear los datos en la tabla
        if(listaAutomoviles != null){
            for(Automovil auto : listaAutomoviles){
                Object[] object = {auto.getId(),auto.getModelo(),auto.getMarca(),
                auto.getMotor(),auto.getColor(),auto.getPlaca(),auto.getNumPuertas()};
                
                modeloTabla.addRow(object);
                
            }
        }
        
        tablaAutos.setModel(modeloTabla);
        
    }
